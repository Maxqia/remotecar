// some common utilities for my programs

const char* ssid = "WIFI_SSID";
const char* password = "WIFI_PASSWORD";
const char WiFiAPPSK[] = "password";

//#define WIFIAPMODE

void connectWifi(void) {
  WiFi.setOutputPower(20.5);
#ifdef WIFIAPMODE // taken from sparkfun's example
  WiFi.mode(WIFI_AP);

  // Do a little work to get a unique-ish name. Append the
  // last two bytes of the MAC (HEX'd) to "Thing-":
  uint8_t mac[WL_MAC_ADDR_LENGTH];
  WiFi.softAPmacAddress(mac);
  String macID = String(mac[WL_MAC_ADDR_LENGTH - 2], HEX) +
                 String(mac[WL_MAC_ADDR_LENGTH - 1], HEX);
  macID.toUpperCase();
  String AP_NameString = "ESP8266 Thing " + macID;

  char AP_NameChar[AP_NameString.length() + 1];
  memset(AP_NameChar, 0, AP_NameString.length() + 1);

  for (int i=0; i<AP_NameString.length(); i++)
    AP_NameChar[i] = AP_NameString.charAt(i);

  WiFi.softAP(AP_NameChar, WiFiAPPSK);
#else
  // Connect to WiFi network
  Serial.println();
  Serial.println();
  Serial.print("Connecting to ");
  Serial.println(ssid);
  
  WiFi.begin(ssid, password); // 8/2
   
  while (WiFi.status() != WL_CONNECTED) {
    delay(450);
    Serial.print(".");
    digitalWrite(statusLED, LOW);
    delay(50);
    digitalWrite(statusLED, HIGH);
  }
  Serial.println("");
  Serial.println("WiFi connected");

  static WiFiEventHandler disconnectedEventHandler = 
  WiFi.onStationModeDisconnected([](const WiFiEventStationModeDisconnected& event){
    Serial.println("Station disconnected, trying to reconnect"); 
    connectWifi();  
  });

#endif
}

